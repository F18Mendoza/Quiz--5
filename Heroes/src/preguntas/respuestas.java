//1. Qué es un interface? 15pts
//
// la interfaz es una construcción del lenguaje que permite declarar un conjunto de constantes y de 
// cabeceras de métodos abstractos (comportamientos) que otras clases van a implementar.
// En estas todos los metodos declarados son publicos y abstractos.
// 

//2. Cuál es la diferencia entre una clase abstracta y un interface? 10pts

// Las clases abstractas representan conceptos generales, engloban las características comunes de
// un conjunto de objetos, su palabra reservada en abstract y no se puede instanciar una clase abstracta.
//
// La interfaz especifica el comportamiento común que tiene un conjunto de clases. 
// Dicho comportamiento se implementa en cada una de las clases
// Y su palabra recervada es interface
// En general las interfaces ayudan a implementar de algún modo la herencia multiple, 
// haciendo que Java posea un mayor nivel de abstracción facilitando la programación